const os = require("os");

// Platform
console.log(os.platform());

// Architecture
console.log(os.arch());

// Information about the prossesor
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Core direction
console.log(os.homedir());

// System up time
console.log(os.uptime());
